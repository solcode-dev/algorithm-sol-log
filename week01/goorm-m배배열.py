n, m = map(int, input().split())
arr = list(map(int, input().split()))

# 각 숫자 변환
for i in range(n):
    if arr[i] % m != 0:  # M으로 안 나누어떨어지면
        arr[i] = arr[i] * m  # M을 곱함

# 출력
print(" ".join(map(str, arr)))


# 실행 과정
# 입력:
# 7 3
# 1 2 3 4 5 6 7

# 동작:
# n = 7, m = 3
# arr = [1, 2, 3, 4, 5, 6, 7]

# num = 1: 1 % 3 = 1 (≠ 0) → 1 × 3 = 3
# num = 2: 2 % 3 = 2 (≠ 0) → 2 × 3 = 6
# num = 3: 3 % 3 = 0 (= 0) → 그대로 3
# num = 4: 4 % 3 = 1 (≠ 0) → 4 × 3 = 12
# num = 5: 5 % 3 = 2 (≠ 0) → 5 × 3 = 15
# num = 6: 6 % 3 = 0 (= 0) → 그대로 6
# num = 7: 7 % 3 = 1 (≠ 0) → 7 × 3 = 21

# result = [3, 6, 3, 12, 15, 6, 21]

# 출력:
# 3 6 3 12 15 6 21
